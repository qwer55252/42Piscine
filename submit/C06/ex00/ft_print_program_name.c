#include <unistd.h>

int	str_len(char *str)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

int	main(int argc, char **argv)
{
	if (argc > 0)
	{
		write(1, argv[0], str_len(argv[0]));
		write(1, "\n", 1);
	}
}
